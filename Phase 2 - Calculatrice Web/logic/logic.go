package logic

func Addition(a, b float64) (somme float64) {
	somme = a + b
	return somme
}

func Soustraction(a, b float64) (difference float64) {
	difference = a - b
	return difference
}

func Multiplication(a, b float64) (produit float64) {
	produit = a * b
	return produit
}

func Division(a, b float64) (quotient float64) {
	quotient = a / b
	return quotient
}
